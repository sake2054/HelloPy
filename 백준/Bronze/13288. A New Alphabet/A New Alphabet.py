rep = {
    "a": "@", "b": "8", "c": "(", "d": "|)", "e": "3", "f": "#", "g": "6", "h": "[-]", "i": "|", "j": "_|", "k": "|<",
    "l": "1", "m": "[]\/[]", "n": "[]\[]", "o": "0", "p": "|D", "q": "(,)", "r": "|Z", "s": "$", "t": "']['", "u": "|_|",
    "v": "\/", "w": "\/\/", "x": "}{", "y": "`/", "z": "2"
}

i = input()
o = ""
for x in range(0, len(i)):
    o = i[x].lower()
    if o in rep:
        o = rep[o]
    print(o, end="")
